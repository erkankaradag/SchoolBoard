using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SchoolBoard
{
    public class Person
    {
        private string Name
        {
            get
            {
                return Name;
            }
            set
            {
                Name = value;
            }
        }
        private string Surname
        {
            get
            {
                return Surname;
            }
            set
            {
                Surname = value;
            }
        }
        private DateTime DateBirth
        {
            get
            {
                return DateBirth;
            }
            set
            {
                DateBirth = value;
            }
        }
    }
}
